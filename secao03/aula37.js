/*
Operadores de comparação

> : Maior que
>= : Maior ou igual a
< : Menor que
<= : Menor ou igual a
== : igualdade (valor)
=== : igualdade estrita (valor e tipo)
!= : diferente de (valor)
!== : diferente estrito (valor e tipo)
*/

const num1 = 10; // number
const num2 = 10; // number
const comp = num1 !== num2;
